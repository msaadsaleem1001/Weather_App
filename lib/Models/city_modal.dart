class CityModal{
  final String cityName;
  final String countryCode;
  final String lat;
  final String lon;
  final bool isDefault;
  bool isSelected;

  CityModal({
    required this.cityName,
    required this.countryCode,
    required this.lat,
    required this.lon,
    required this.isDefault,
    required this.isSelected
  });
}

















